#include <libft.h>

int		ft_swap_bytes(int nb)
{
	int		nb_swapped;

	nb_swapped = ((nb & 0x000000FF) << 24) |
		((nb & 0x0000FF00) << 8) |
		((nb & 0x00FF0000) >> 8) |
		((nb & 0xFF000000) >> 24);
	return (nb_swapped);
}
